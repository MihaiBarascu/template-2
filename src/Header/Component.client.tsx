export const HeaderClient: React.FC<HeaderClientProps> = ({ data }) => {
  const [mobileMenuOpen, setMobileMenuOpen] = useState(false)
  const pathname = usePathname()

  useEffect(() => {
    setMobileMenuOpen(false)
  }, [pathname])

  const socialLinks = data?.socialLinks as Array<{ platform: string; url: string }> | undefined

  return (
    <header className="relative z-20 bg-gymso-dark">
      <div className="container mx-auto max-w-6.5xl">
        <nav className="flex items-center justify-between h-20 px-4">
          {/* Logo */}
          <Link
            href="/"
            className="text-3xl font-bold text-white hover:text-gymso-primary transition-colors"
          >
            Gymso Fitness
          </Link>

          {/* Desktop Navigation */}
          <div className="hidden lg:flex items-center">
            <HeaderNav data={data} className="flex items-center space-x-2" />

            {/* Social Icons */}
            {socialLinks && socialLinks.length > 0 && (
              <div className="flex items-center ml-8 space-x-4">
                {socialLinks.map((social, index) => (
                  <a
                    key={index}
                    href={social.url}
                    target="_blank"
                    rel="noopener noreferrer"
                    className="text-white hover:text-gymso-primary transition-colors"
                    aria-label={social.platform}
                  >
                    <svg
                      className="w-5 h-5"
                      fill="currentColor"
                      viewBox="0 0 24 24"
                      aria-hidden="true"
                    >
                      <path d={socialIcons[social.platform]} />
                    </svg>
                  </a>
                ))}
              </div>
            )}
          </div>

          {/* Mobile Menu Button */}
          <button
            onClick={() => setMobileMenuOpen(!mobileMenuOpen)}
            className="lg:hidden text-white hover:text-gymso-primary transition-colors"
            aria-label="Toggle menu"
          >
            {mobileMenuOpen ? <X className="h-6 w-6" /> : <Menu className="h-6 w-6" />}
          </button>
        </nav>

        {/* Mobile Navigation */}
        <div
          className={`lg:hidden transition-all duration-300 overflow-hidden ${
            mobileMenuOpen ? 'max-h-96 pb-6' : 'max-h-0'
          }`}
        >
          <HeaderNav data={data} className="flex flex-col space-y-4 pt-4" />

          {/* Mobile Social Icons */}
          {socialLinks && socialLinks.length > 0 && (
            <div className="flex items-center space-x-4 pt-4 mt-4 border-t border-gray-700">
              {socialLinks.map((social, index) => (
                <a
                  key={index}
                  href={social.url}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="text-white hover:text-gymso-primary transition-colors"
                  aria-label={social.platform}
                >
                  <svg
                    className="w-5 h-5"
                    fill="currentColor"
                    viewBox="0 0 24 24"
                    aria-hidden="true"
                  >
                    <path d={socialIcons[social.platform]} />
                  </svg>
                </a>
              ))}
            </div>
          )}
        </div>
      </div>
    </header>
  )
}
