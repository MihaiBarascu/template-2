// Social platforms that can be linked (subset of all icons)
export const socialPlatforms = [
  'facebook',
  'instagram',
  'twitter',
  'linkedin',
  'youtube',
  'tiktok',
  'whatsapp',
] as const

export type SocialPlatform = (typeof socialPlatforms)[number]

// Get social URL from BusinessInfo
export const getSocialUrl = (platform: string, businessInfo: BusinessInfo): string | null => {
  const urlMap: { [key: string]: string | null | undefined } = {
    facebook: businessInfo?.facebook,
    instagram: businessInfo?.instagram,
    twitter: businessInfo?.twitter,
    linkedin: businessInfo?.linkedin,
    youtube: businessInfo?.youtube,
    tiktok: businessInfo?.tiktok,
    whatsapp: businessInfo?.whatsapp
      ? `https://wa.me/${businessInfo.whatsapp.replace(/\D/g, '')}`
      : null,
  }
  return urlMap[platform] || null
}

// Build social links array from BusinessInfo (for components that need all links)
export const buildSocialLinks = (
  businessInfo: BusinessInfo,
  platforms?: string[],
): Array<{ platform: string; url: string }> => {
  const platformsToUse = platforms || socialPlatforms

  return platformsToUse
    .map((platform) => {
      const url = getSocialUrl(platform, businessInfo)
      if (!url) return null
      return { platform, url }
    })
    .filter(Boolean) as Array<{ platform: string; url: string }>
}
